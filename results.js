// スコアから判定結果を取得する関数
function getResult(happinessScore, affectionScore) {
    if (happinessScore >= 5 && affectionScore >= 5) {
        return resultsData.happyAffection;
    } else if (happinessScore >= 5 && affectionScore < 5) {
        return resultsData.happyEvaluation;
    } else if (happinessScore < 5 && affectionScore >= 5) {
        return resultsData.unhappyAffection;
    } else {
        return resultsData.unhappyEvaluation;
    }
}

// スコアの表示テキストを取得する関数
function getScoreText(score, type) {
    if (type === 'happiness') {
        if (score >= 15) return '超幸せ体質';
        if (score >= 5) return '幸せ体質';
        if (score >= -5) return '中立';
        if (score >= -15) return '不幸体質';
        return '超不幸体質';
    } else { // affection
        if (score >= 15) return '超愛情型';
        if (score >= 5) return '愛情型';
        if (score >= -5) return '中立';
        if (score >= -15) return '評価型';
        return '超評価型';
    }
}

